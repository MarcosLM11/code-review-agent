package com.marcos.codereviewagent.domain.service;

import com.marcos.codereviewagent.domain.model.CodeAnalysis;
import com.marcos.codereviewagent.domain.model.CodeIssue;
import com.marcos.codereviewagent.domain.model.IssueSeverity;
import com.marcos.codereviewagent.domain.model.PrDiff;
import com.marcos.codereviewagent.domain.model.ReviewSuggestion;
import org.springframework.stereotype.Service;
import static com.marcos.codereviewagent.domain.model.IssueSeverity.CRITICAL;
import static com.marcos.codereviewagent.domain.model.IssueSeverity.WARNING;

@Service
public class ReviewFormatter {

    public String formatSummary(PrDiff diff) {
        var sb = new StringBuilder();
        sb.append("## PR Summary: %s\n\n".formatted(diff.title()));
        sb.append("**Branch:** `%s` → `%s`\n\n".formatted(diff.headBranch(), diff.baseBranch()));
        sb.append("### Files Changed (%d)\n".formatted(diff.files().size()));

        int totalAdded = 0;
        int totalDeleted = 0;
        for (var file : diff.files()) {
            sb.append("- `%s` [%s] +%d/-%d\n".formatted(
                    file.filename(), file.status(), file.additions(), file.deletions()));
            totalAdded += file.additions();
            totalDeleted += file.deletions();
        }

        sb.append("\n**Total:** +%d/-%d lines\n".formatted(totalAdded, totalDeleted));
        sb.append("\n---\n*Generated by CODE Review Agent (Embabel + Spring AI)*");
        return sb.toString();
    }

    public String format(PrDiff diff, CodeAnalysis analysis) {
        var sb = new StringBuilder();
        sb.append("## AI Code Review: %s\n\n".formatted(diff.title()));
        sb.append("### Overview\n%s\n\n".formatted(analysis.overallAssessment()));

        appendIssues(sb, analysis);
        appendSuggestions(sb, analysis);

        sb.append("\n---\n*Generated by CODE Review Agent (Embabel + Spring AI)*");
        return sb.toString();
    }

    private void appendIssues(StringBuilder sb, CodeAnalysis analysis) {
        if (analysis.issues().isEmpty()) {
            return;
        }
        sb.append("### Issues Found\n");
        for (CodeIssue issue : analysis.issues()) {
            var icon = severityIcon(issue.severity());
            sb.append("- %s **[%s]** `%s` (%s): %s\n".formatted(
                    icon,
                    issue.severity().name(),
                    issue.filename(),
                    issue.lineReference(),
                    issue.description()));
        }
        sb.append("\n");
    }

    private void appendSuggestions(StringBuilder sb, CodeAnalysis analysis) {
        if (analysis.suggestions().isEmpty()) {
            return;
        }
        sb.append("### Suggestions\n");
        for (ReviewSuggestion suggestion : analysis.suggestions()) {
            sb.append("- `%s`: %s\n".formatted(
                    suggestion.filename(), suggestion.description()));
            if (suggestion.suggestedCode() != null) {
                sb.append("  ```\n  %s\n  ```\n".formatted(suggestion.suggestedCode()));
            }
        }
    }

    private String severityIcon(IssueSeverity severity) {
        if (severity == CRITICAL) {
            return "[CRITICAL]";
        }
        if (severity == WARNING) {
            return "[WARNING]";
        }
        return "[INFO]";
    }
}
